char rx_data[128] = {0};	// 记得加终止符
uint8_t ptr = 0;
char data;

void uart_init(void)
{
	HAL_UART_Receive_IT(&huart1, (uint8_t *)&data, 1);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART1)
	{
		rx_data[ptr++] = data;
		HAL_UART_Receive_IT(&huart1, (uint8_t *)&data, 1);
	}
}

extern uint8_t X, Y;
extern float pa1_fre;
extern float PA4, PA5;
extern uint8_t lcd_mode;

void uart_task(void)
{
	if(ptr > 0)
	{
		uint8_t tmp = ptr;
		HAL_Delay(1);
		if(ptr == tmp)
		{
			// 查询参数 & 屏幕反转
			if(ptr == 1)
			{
				if(rx_data[0] == 'X')
				{
					printf("X:%d",X);
				}
				else if(rx_data[0] == 'Y')
				{
					printf("Y:%d",Y);
				}
				else if(rx_data[0] == '#')
				{
					lcd_mode ^= 1;
				}
			}
			else if(ptr == 3)
			{
				// 查询数据
				if(rx_data[0] == 'P' && rx_data[1] == 'A' && rx_data[2] == '1')
				{
					printf("PA1:%d", (uint32_t)pa1_fre);
				}
				else if(rx_data[0] == 'P' && rx_data[1] == 'A' && rx_data[2] == '4')
				{
					printf("PA4:%.2f", PA4);
				}
				else if(rx_data[0] == 'P' && rx_data[1] == 'A' && rx_data[2] == '5')
				{
					printf("PA5:%.2f", PA5);
				}
			}
			
			ptr = 0;
			memset(rx_data, 0, 128);
		}
	}
}
