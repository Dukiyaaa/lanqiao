uint32_t adc_val[1];
void adc_init()
{
	HAL_ADCEx_Calibration_Start(&hadc2, ADC_SINGLE_ENDED);
	HAL_ADC_Start_DMA(&hadc2, adc_val, 1);
}

uint8_t dutyA, dutyB;
uint8_t dutyA_buf[6] = {10, 30, 50, 70, 90, 95};
uint8_t dutyB_buf[6] = {5, 25, 45, 65, 85, 90};
float V;
void adc_task(void)
{
	V = adc_val[0] * 3.3f / 4095.0f;
	
	if(V < 0.5f)
	{
		dutyA = dutyA_buf[0];
		dutyB = dutyB_buf[0];
	}
	else if(V >= 0.5f && (V < 1.0f))
	{
		dutyA = dutyA_buf[1];
		dutyB = dutyB_buf[1];
	}
	else if(V >= 1.0f && (V < 1.5f))
	{
		dutyA = dutyA_buf[2];
		dutyB = dutyB_buf[2];
	}
	else if(V >= 1.5f && (V < 2.0f))
	{
		dutyA = dutyA_buf[3];
		dutyB = dutyB_buf[3];
	}
	else if(V >= 2.0f && (V < 2.5f))
	{
		dutyA = dutyA_buf[4];
		dutyB = dutyB_buf[4];
	}
	else
	{
		dutyA = dutyA_buf[5];
		dutyB = dutyB_buf[5];
	}
	
}
